# 1. Arithematic operators
# + plus: 9+1=10
# - minus: 5-3=2
# * multiply: 5*3=15
# / divide : 8/2=4
# % modulus: it gives reminder value like: 5 % 2 = 1 , 6 % 2 = 0
# **  exponential: 2**2 = 4, 3**3=27
# // floor: give division in floor amount like if 15//2 = 7.5 but floor will give 7
print(9+1)
print(5-3)
print(5*3)
print(8/2)
print(5 % 2)
print(6 % 2)
print(2**2)
print(3**3)
